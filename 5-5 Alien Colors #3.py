alien_color = ['green', 'yellow', 'red']

for color in alien_color:
    if color == 'green':
        print(f"If you shoot green colored Alien, You get 5 points.")
    elif color == 'yellow':
        print(f"If you shoot yellow colored Alien, You get 10 points.")
    elif color == 'red':
        print(f"If you shoot red colored Alien, You get 15 points.")